declare(strict_types=1);

namespace Pratech\Order\Controller\Adminhtml\Shipment\TrackUpdates;

class Index extends \Magento\Backend\App\Action
{

    /**
     * Constructor
     *
     * @param \Magento\Backend\App\Action\Context $context
     * @param \Magento\Framework\View\Result\PageFactory $resultPageFactory
     */
    public function __construct(
        \Magento\Backend\App\Action\Context $context,
        protected \Magento\Framework\View\Result\PageFactory $resultPageFactory
    ) {
        parent::__construct($context);
    }

    /**
     * Index action
     *
     * @return \Magento\Framework\Controller\ResultInterface
     */
    public function execute()
    {
        $resultPage = $this->resultPageFactory->create();
        $resultPage->addHandle('sales_shipment_track_updates_index');
        $resultPage->setActiveMenu('Pratech_Order::shipment_track_updates');
        $resultPage->getConfig()->getTitle()->prepend(__("Shipment Track Updates"));
        return $resultPage;
    }

    /**
     * Is Allowed
     *
     * @return bool
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('Pratech_Order::shipment_track_updates');
    }
}
